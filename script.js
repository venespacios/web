/* ── UTILS ──────────────────────────────────────────────────────── */
function slugify(str) {
  return str.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim().replace(/\s+/g, '-');
}

const todos = Object.entries(DATA).flatMap(([id, st]) =>
  st.lugares.map(l => ({ ...l, estadoId: id, estadoNombre: st.nombre, slug: slugify(l.n) }))
);

const ICONS = {
  Natural:'🌿', Histórico:'🏛', Cultural:'🎭',
  Religioso:'⛪', UNESCO:'🏆', Patrimonio:'🗼'
};

/* ── GALLERY STATE ──────────────────────────────────────────────── */
let currentData  = [];
const PAGE_SIZE  = 6;
let   shownCount = PAGE_SIZE;

function cardHTML(l) {
  const thumb = l.img
    ? `<img src="${l.img}" alt="${l.n}" style="width:100%;height:100%;object-fit:cover;">`
    : `<div class="card-thumb-icon">${ICONS[l.cat] || '📍'}</div>`;
  return `
    <div class="card" onclick="location.href='lugar.html?estado=${l.estadoId}&lugar=${l.slug}'">
      <div class="card-thumb">${thumb}</div>
      <span class="card-state">${l.estadoNombre}</span>
      <span class="card-cat cat-${l.cat}">${l.cat}</span>
      <h3>${l.n}</h3>
      <p>${l.d}</p>
    </div>`;
}

function render(data) {
  const gallery = document.getElementById('placesGallery');
  if (!gallery) return;
  currentData  = data;
  shownCount   = PAGE_SIZE;
  gallery.innerHTML = data.slice(0, PAGE_SIZE).map(cardHTML).join('');
  updateVerMasBtn();
}

function verMas() {
  const gallery = document.getElementById('placesGallery');
  if (!gallery) return;
  const next = currentData.slice(shownCount, shownCount + PAGE_SIZE);
  gallery.insertAdjacentHTML('beforeend', next.map(cardHTML).join(''));
  shownCount += next.length;
  updateVerMasBtn();
}

function updateVerMasBtn() {
  const btn = document.getElementById('verMasBtn');
  if (!btn) return;
  if (shownCount >= currentData.length) {
    btn.style.display = 'none';
  } else {
    btn.style.display = 'inline-block';
    btn.textContent   = `Ver más (${currentData.length - shownCount} restantes)`;
  }
}

/* ── FILTERS ─────────────────────────────────────────────────────── */
function filterBy(criteria, btn) {
  document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
  if (btn) btn.classList.add('active');
  // Clear search input if exists
  const si = document.getElementById('searchInput');
  if (si) si.value = '';
  let data = [...todos];
  if (criteria === 'nombre') data.sort((a, b) => a.n.localeCompare(b.n));
  if (criteria === 'estado') data.sort((a, b) => a.estadoNombre.localeCompare(b.estadoNombre));
  render(data);
}
/*FUNCION RANDOM PARA EL BOTON EXPLORAR */
function explorarRandom() {
  const all = Object.entries(DATA).flatMap(([estadoId, st]) =>
    st.lugares.map(l => ({ estadoId, slug: slugify(l.n) }))
  );
  const pick = all[Math.floor(Math.random() * all.length)];
  window.location.href = `lugar.html?estado=${pick.estadoId}&lugar=${pick.slug}`;
}

/* ── SEARCH ──────────────────────────────────────────────────────── */
function buscar(q) {
  // Deactivate filter buttons
  document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
  q = q.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  if (!q) { render(todos); return; }
  const results = todos.filter(l => {
    const name   = l.n.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    const estado = l.estadoNombre.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    const cat    = l.cat.toLowerCase();
    const desc   = l.d.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    return name.includes(q) || estado.includes(q) || cat.includes(q) || desc.includes(q);
  });
  render(results);
  // Show "no results" if needed
  const gallery = document.getElementById('placesGallery');
  if (gallery && results.length === 0) {
    gallery.innerHTML = `<p style="color:var(--muted);grid-column:1/-1;padding:40px 0;">
      No se encontraron resultados para "<strong>${q}</strong>".
    </p>`;
    const btn = document.getElementById('verMasBtn');
    if (btn) btn.style.display = 'none';
  }
}

/* ── MAP INTERACTIONS ────────────────────────────────────────────── */
const tooltip = document.getElementById('mapTooltip');

document.querySelectorAll('.estado').forEach(el => {
  el.addEventListener('mouseenter', e => {
    const nombre  = el.getAttribute('data-nombre');
    const capital = el.getAttribute('data-capital');
    if (tooltip) {
      tooltip.innerHTML = `<strong>${nombre}</strong>Cap. ${capital}`;
      tooltip.classList.add('show');
    }
    document.querySelectorAll('.estado').forEach(s => s.classList.remove('selected'));
    document.querySelectorAll('.estado-lbl').forEach(t => t.classList.remove('selected'));
    el.classList.add('selected');
    const lbl = document.getElementById('lbl_' + el.id);
    if (lbl) lbl.classList.add('selected');
  });

  el.addEventListener('mousemove', e => {
    if (tooltip) {
      tooltip.style.left = (e.clientX + 16) + 'px';
      tooltip.style.top  = (e.clientY - 12) + 'px';
    }
  });

  el.addEventListener('mouseleave', () => {
    if (tooltip) tooltip.classList.remove('show');
    el.classList.remove('selected');
    const lbl = document.getElementById('lbl_' + el.id);
    if (lbl) lbl.classList.remove('selected');
  });

  el.addEventListener('click', () => {
    window.location.href = `estado.html?estado=${el.id}`;
  });
});

